public class VictorCarvalho_Exercício04{
    public static void main (String [] args){
       
       //Declaração de variáveis
        int cont;

        //Inicialização das variáveis
        cont=0;
        
        //Alteração para cont funcionar
        cont +=1;

        //Etrutura de repetição 
        while(cont<1000){

            //Saídas
            System.out.println(cont);
            cont+=2;
            
        }
    }
}